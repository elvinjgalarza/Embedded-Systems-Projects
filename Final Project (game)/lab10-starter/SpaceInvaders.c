int delay1;
void PortE_Init(void){
	SYSCTL_RCGCGPIO_R |= 0x00000010;					//activating clock for portE
	delay1 = SYSCTL_RCGCGPIO_R;
	delay1 = SYSCTL_RCGCGPIO_R;
	delay1 = SYSCTL_RCGCGPIO_R;
	GPIO_PORTE_DIR_R &= ~0x03;					//making PE1 & PE0 input for switches
	GPIO_PORTE_PCTL_R &= ~0x0000000F;
	GPIO_PORTE_AFSEL_R &= ~0x03;
	GPIO_PORTE_AMSEL_R &= ~0x03;
	GPIO_PORTE_DEN_R |= 0x03;   
}
// Initialize Port F so PF1, PF2 and PF3 are heartbeats
void PortF_Init(void){
 SYSCTL_RCGCGPIO_R |= 0x00000020;					//activating clock for portF
	delay1 = SYSCTL_RCGCGPIO_R;
	delay1 = SYSCTL_RCGCGPIO_R;
	delay1 = SYSCTL_RCGCGPIO_R;
 GPIO_PORTF_AMSEL_R &= ~0x00;     
 GPIO_PORTF_PCTL_R &= ~0x0F; // regular GPIO function
 GPIO_PORTF_DIR_R |= 0x0E;					// make PF1-3 output
 GPIO_PORTF_AFSEL_R &= ~0x0E;   // disable alt funct on PF1-3
 GPIO_PORTF_DEN_R |= 0x0E;      // enable digital I/O on PF1-3	
}

uint32_t Convert(uint32_t input){
  input=input*249;	
	input = input / 10000;
	
  return input;
}

void SysTick_Init(int reload){
		NVIC_ST_CTRL_R = 0;         // disable SysTick during setup
		NVIC_ST_RELOAD_R = reload-1;// reload value
		NVIC_ST_CURRENT_R = 0;      // any write to current clears it
		NVIC_SYS_PRI3_R = (NVIC_SYS_PRI3_R&0x00FFFFFF)|0x20000000; // priority 1
		NVIC_ST_CTRL_R = 0x07; // enable SysTick with core clock and interrupts
}

