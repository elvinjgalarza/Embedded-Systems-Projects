int CheckUp(void){															//checking if up switch is pressed
	int checkup = 0;
	checkup = GPIO_PORTE_DATA_R & 0x03;
	if(checkup == 0x01){
		if( y < 10){													//keeping frog in bounds if it's near border of screen
			frogposy  = frogposy;
		}
		else{
			frogposy = frogposy - 22;
		}
		return 1;
	}
	else{
		return 0;}
}


int CheckDown(void){												//checking if down switch is pressed
	int checkdown = 0;
	checkdown = GPIO_PORTE_DATA_R & 0x03;
	if(checkdown == 0x02){
		if( y >= 159){													//keeping frog in bounds if it's near border of screen
			frogposy  = frogposy;
		}
		else{
			frogposy = frogposy + 22;
		}
		return 1;
	}
	else{
		return 0;}
}
	
