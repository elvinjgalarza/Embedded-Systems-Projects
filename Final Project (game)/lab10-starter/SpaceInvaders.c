int UpdateCarX(signed int carx, int dir){						//calculating new x value for sprites
	if(dir == 1){							//left car
		carx = carx -1;
		if(carx < 0 ){
			carx = 122;
			eraseleft = 1;
			dir = 0;
	}
	return carx;
}
	else if(dir == 0){					//right car
		carx = carx +1;
		if(carx > 128){
			carx = 0;
			dir = 1;
		}
		return carx;
	}
	else{
	return carx;}
}

